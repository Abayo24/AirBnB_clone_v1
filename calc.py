class Calc:
    """calculator that adds subtracts, multiplies and divides only"""
    def add(a, b):
        """adds two numbers"""
        return a + b

    def subtract(a, b):
        """subtracts b from a"""
        return a - b

    def multiply(a, b):
        """multiplies two numbers"""
        return a * b

    def divide(a, b):
        """divides a by b"""
        return a / b
